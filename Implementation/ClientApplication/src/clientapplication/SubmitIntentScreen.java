    public void fillSelections(List<TravelDetails> travelDetails) {
        this.travelDetails = travelDetails;
        messageIDCombo.removeAllItems();
        
        for (TravelDetails trip : this.travelDetails) {
            if (!trip.userID.equals(ClientApplication.userID)){
                messageIDCombo.addItem(trip.messageID);
            }
        }
    }
    
