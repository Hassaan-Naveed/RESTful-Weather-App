    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String selection = messageIDCombo.getSelectedItem().toString();
        
        if (!"".equals(selection) && !this.travelDetails.isEmpty()) {
            for (TravelDetails trip : this.travelDetails) {
                
                if (trip.messageID.equals(selection)) {
                    String messageID = trip.messageID;
                    String ownerID = trip.userID;
                    String responderID = ClientApplication.userID;
                    
                    String json = String.format("{'userID': '%s', 'messageID': '%s', 'responderID': '%s'}", ownerID, messageID, responderID);
                    try {
                        MainScreen.setIntentDetails(json);
                    } catch (MalformedURLException ex) {
                        Logger.getLogger(SubmitIntentScreen.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    this.setVisible(false);
                    
                }
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

